namespace DynamixLogger
{
    public class DateFormat
    {

        /// <summary>
        ///  Format: "6/15/2008"
        /// </summary>
        public const string d = "d";


        /// <summary>
        /// Format: Sunday, June 15, 2008
        /// </summary>
        public const string D = "D";


        /// <summary>
        /// Format: "Sunday, June 15, 2008 9:15 PM"
        /// </summary>
        public const string f = "f";


        /// <summary>
        /// Format: "Sunday, June 15, 2008 9:15:07 PM"
        /// </summary>
        public const string F = "F";


        /// <summary>
        /// Format: "6/15/2008 9:15 PM"
        /// </summary>
        public const string g = "g";


        /// <summary>
        /// Format: "6/15/2008 9:15:07 PM"
        /// </summary>
        public const string G = "G";


        /// <summary>
        /// Format: "June 15"
        /// </summary>
        public const string m = "m";


        /// <summary>
        /// Format: "2008-06-15T21:15:07.0000000"
        /// </summary>
        public const string o = "o";


        /// <summary>
        /// Format: "Sun, 15 Jun 2008 21:15:07 GMT"
        /// </summary>
        public const string R = "R";


        /// <summary>
        /// Format: "2008-06-15T21:15:07"
        /// </summary>
        public const string s = "s";


        /// <summary>
        /// Format: "9:15 PM"
        /// </summary>
        public const string t = "t";


        /// <summary>
        /// Format: "9:15:07 PM"
        /// </summary>
        public const string T = "T";


        /// <summary>
        /// Format: "2008-06-15 21:15:07Z"
        /// </summary>
        public const string u = "u";


        /// <summary>
        /// Format: "Monday, June 16, 2008 4:15:07 AM"
        /// </summary>
        public const string U = "U";



        /// <summary>
        /// Format: "June, 2008"
        /// </summary>
        public const string y = "Y";


    }
}
